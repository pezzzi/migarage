      <div class="respuesta">Mi garage es una plataforma que te permite, si tenés un vehículo y buscás dónde dejarlo, encontrar un garage donde más lo necesitas. Cerca de tu casa, del trabajo, dónde sea. En tanto, si sos dueño de un garage y no lo estás usando, o querés ganar algo de dinero con él, Mi Garage te permite, sin costos ni comisiones, ponerlo en alquiler. Así de simple.</div>

      <div class="preguntafaqs">Tengo un auto y necesito un garage. ¿Cómo busco un aviso?</div>
      <div class="respuesta">Ingresando a nuestros listados aplicando los filtros que más te convengan: ubicación (provincia, barrio), tipo de vehículo que vas a guardar (moto, auto chico, camioneta, etc.), características del garage (cubierto, semi cubierto, descubierto) o su forma de acceso (con portón, en estacionamiento libre, cochera subterránea, etc.), precio máximo, duración del alquiler y demás opciones. Esto te permitirá encontrar más fácil lo que estás buscando.</div>

      <div class="preguntafaqs">Ya encontré lo que busco. ¿Cómo sigo?</div>
      <div class="respuesta">Si encontraste un Garage que te sirve, el primer paso es Registrarte como usuario. Esto te abrirá la posibilidad de obtener los datos del dueño y contactarlo, ya sea por medio del formulario de contacto del aviso o por los datos que haya dejado el propietario (teléfono, email). Es condición estar registrado para ver estos datos.</div>

      <div class="preguntafaqs">¿Puedo ver avisos en oferta sin estar registrado?</div>
      <div class="respuesta">Sí, vas a poder navegar por todo el sitio y hacer las búsquedas que quieras. Sin embargo, no vas a poder acceder a los datos del propietario hasta que estés registrado como usuario en Mi Garage.</div>

      <div class="preguntafaqs">¿Cobran alguna comisión, a cualquiera de las partes que intervienen?</div>
      <div class="respuesta">No, Mi Garage basa su negocio en la venta de publicidad. No cobramos comisiones por nuestro servicio.</div>

      <div class="preguntafaqs">¿Cómo se pagan los alquileres de los garages en oferta?</div>
      <div class="respuesta">Mi Garage no interviene en este punto. Está sujeto a las opciones que brinde el dueño (efectivo, transferencia bancaria mensual, tarjeta de crédito, etc.). Mi Garage no interviene ni tiene responsabilidad en la relación contractual entre ambas partes.</div>

      <div class="preguntafaqs">¿Se pueden comprar o vender las propiedades que están publicadas?</div>
      <div class="respuesta">MiGarage solo publica ofertas de alquiler. No hay operaciones de compra/venta ofertadas en la plataforma. De todas formas, avanzar a cualquiera de estas operaciones comerciales es completa decisión del dueño y de quien reserva.</div>

      <div class="preguntafaqs">Tengo un garage y quiero ofrecerlo. ¿Cómo hago?</div>
